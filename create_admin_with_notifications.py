    logger.info("üöÄ –°–æ–∑–¥–∞–Ω–∏–µ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞ —Å —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è–º–∏")
